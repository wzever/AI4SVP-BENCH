def calculate_statistics(data, dimensions, methods):
    """
    Calculate statistics for each algorithm at each dimension
    """
    results = []
    
    for method in methods:
        for dim in dimensions:
            norms = data[method][dim]['norm']
            times = data[method][dim]['time']
            
            if norms and times:  # Ensure there is data
                # Calculate norm statistics
                norm_mean = np.mean(norms)
                norm_std = np.std(norms)
                norm_cv = norm_std / norm_mean if norm_mean != 0 else 0
                
                # Calculate time statistics
                time_mean = np.mean(times)
                time_std = np.std(times)
                time_cv = time_std / time_mean if time_mean != 0 else 0
                
                results.append({
                    'Method': method,
                    'Dimension': dim,
                    'Norm_Mean': norm_mean,
                    'Norm_Std': norm_std,
                    'Norm_CV': norm_cv,
                    'Time_Mean': time_mean,
                    'Time_Std': time_std,
                    'Time_CV': time_cv,
                    'Num_Samples': len(norms)
                })
    
    return pd.DataFrame(results)

def create_summary_table(df):
    """
    Create algorithm stability summary table
    """
    summary_data = []
    
    methods = df['Method'].unique()
    
    for method in methods:
        method_df = df[df['Method'] == method]
        
        # Calculate average and standard deviation of norm coefficient of variation
        avg_norm_cv = method_df['Norm_CV'].mean()
        std_norm_cv = method_df['Norm_CV'].std()
        
        # Calculate average and standard deviation of time coefficient of variation
        avg_time_cv = method_df['Time_CV'].mean()
        std_time_cv = method_df['Time_CV'].std()
        
        # Calculate average norm and average time (average across all dimensions)
        avg_norm = method_df['Norm_Mean'].mean()
        avg_time = method_df['Time_Mean'].mean()
        
        # Determine stability level
        stability_level = "Highly Stable"
        if avg_norm_cv > 0.15 or avg_time_cv > 0.3:
            stability_level = "High Variability"
        elif avg_norm_cv > 0.1 or avg_time_cv > 0.2:
            stability_level = "Moderately Stable"
        elif avg_norm_cv > 0.05 or avg_time_cv > 0.1:
            stability_level = "Stable"
        
        summary_data.append({
            'Method': method,
            'Avg_Norm': f"{avg_norm:.1f}",
            'Avg_Time': f"{avg_time:.4f}",
            'Avg_Norm_CV': f"{avg_norm_cv:.4f}",
            'Norm_CV_Std': f"{std_norm_cv:.4f}",
            'Avg_Time_CV': f"{avg_time_cv:.4f}",
            'Time_CV_Std': f"{std_time_cv:.4f}",
            'Stability': stability_level,
            'Num_Dims': len(method_df)
        })
    
    summary_df = pd.DataFrame(summary_data)
    
    # Sort by time coefficient of variation (variability from smallest to largest)
    summary_df = summary_df.sort_values('Avg_Time_CV')
    
    return summary_df

